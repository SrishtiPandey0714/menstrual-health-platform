// Type for nested object keys
export type TranslationKey = string;

// Flatten nested object to dot notation
export function flattenTranslations(obj: any, prefix = ''): Record<string, string> {
    return Object.keys(obj).reduce((acc: Record<string, string>, key) => {
        const pre = prefix.length ? `${prefix}.` : '';
        if (typeof obj[key] === 'object' && obj[key] !== null && !Array.isArray(obj[key])) {
            Object.assign(acc, flattenTranslations(obj[key], pre + key));
        } else {
            acc[pre + key] = obj[key];
        }
        return acc;
    }, {});
}

// Get all translation strings as flat array
export function getAllTranslationStrings(): string[] {
    const flat = flattenTranslations(defaultTranslations);
    return Object.values(flat);
}

// Get translation keys
export function getAllTranslationKeys(): string[] {
    const flat = flattenTranslations(defaultTranslations);
    return Object.keys(flat);
}
